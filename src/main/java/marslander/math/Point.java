package marslander.math;

import java.util.Objects;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /**
     * Permet de recuperer un point sur une droite (origin;extremite)
     * Ã  distance length du point origin.
     */
    public static Point getPointOnLine(Point origin, Point extremite, double length) {
        Vector vector = new Vector(origin, extremite);
        vector.normalize();
        vector.scale(length);
        vector.add(origin);
        return new Point(vector.getX(), vector.getY());
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Point point = (Point) o;
        return x == point.x && y == point.y;
    }

    @Override
    public int hashCode() {
        return Objects.hash(x, y);
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
